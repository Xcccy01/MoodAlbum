import { chromium, devices, request as playwrightRequest } from "@playwright/test";
const stamp = Date.now().toString(36);
const username = `seen_${stamp}`;
const password = "seen1234";
const userApi = await playwrightRequest.newContext({ baseURL: "http://YOUR_SERVER_IP" });
await userApi.post("/api/auth/register", { data: { username, password } });
await userApi.post("/api/moods", { data: { moodKey: "bad" } });
const adminApi = await playwrightRequest.newContext({ baseURL: "http://YOUR_SERVER_IP" });
await adminApi.post("/api/admin/login", { data: { passcode: process.env.ADMIN_PASSCODE } });
const list = await (await adminApi.get("/api/admin/moods?status=pending&limit=60")).json();
const target = list.items.find((item) => item.user?.username === username);
await adminApi.post(`/api/admin/moods/${target.id}/replies`, { data: { content: "已读状态自动验证" } });
const before = await (await adminApi.get(`/api/admin/moods/${target.id}`)).json();
const browser = await chromium.launch({ channel: "msedge", headless: true });
const context = await browser.newContext({ ...devices["iPhone 13"] });
const page = await context.newPage();
await page.goto("http://YOUR_SERVER_IP/");
await page.getByPlaceholder("2 到 20 位中文、字母、数字、下划线或短横线").fill(username);
await page.getByPlaceholder("至少 6 位").fill(password);
await page.getByRole("button", { name: "进入应用" }).click();
await page.getByRole("button", { name: /☘️ 心情/ }).click();
const hasSeenButton = await page.getByRole("button", { name: "已查看" }).count();
await page.waitForTimeout(1500);
await browser.close();
const after = await (await adminApi.get(`/api/admin/moods/${target.id}`)).json();
console.log(JSON.stringify({ unreadBefore: before.mood.unreadReplyCount, unreadAfter: after.mood.unreadReplyCount, hasSeenButton }, null, 2));
